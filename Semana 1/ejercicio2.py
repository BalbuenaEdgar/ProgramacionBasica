

print('Comparación de Números')

numA = int(input('\nIngrese el Primer Numero: '))
numB = int(input('Ingrese el Segundo Numero: '))

if (numA == numB):
    print('\nAmbos numeros son iguales')
elif (numA > numB):
    print('\nEl primer numero es mayor')
else:
    print('\nEl segundo numero es mayor')